import { readFile } from "node:fs/promises";

export function canExportArtifact(artifact) {
  return artifact?.kind === "glyph-svg" && Boolean(artifact.path);
}

export async function buildArtifactExport(artifact, format) {
  if (!canExportArtifact(artifact)) return null;

  const source = sanitizeSvgContent(await readFile(artifact.path, "utf8"));
  if (!source) return null;

  const basename = slugify(artifact.title || "glyph");
  if (format === "svg") {
    return {
      filename: `${basename}-mark.svg`,
      contentType: "image/svg+xml; charset=utf-8",
      body: source
    };
  }

  if (format === "font-svg") {
    return {
      filename: `${basename}-single-glyph-font.svg`,
      contentType: "image/svg+xml; charset=utf-8",
      body: buildSvgFontProof(artifact, source)
    };
  }

  return null;
}

function buildSvgFontProof(artifact, source) {
  const fontId = `${slugify(artifact.title || "creative-ip")}-font`;
  const family = `${titleCase(artifact.title || "Creative IP")} Export`;
  const glyphPath = extractPathData(source).join(" ") || "M180 760 L180 180 L500 760 L820 180 L820 760";
  const embeddedSource = source.replace("<svg", "<svg x=\"560\" y=\"120\" width=\"280\" height=\"344\"");

  return `<?xml version="1.0" encoding="UTF-8"?>
<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 1100 720" role="img" aria-label="${escapeXml(family)} single glyph export">
  <title>${escapeXml(family)} single glyph export</title>
  <desc>Export proof generated by Creative IP Studio. The SVG mark is production-usable; the SVG font maps uppercase N as a minimal handoff artifact.</desc>
  <defs>
    <font id="${escapeXml(fontId)}" horiz-adv-x="1000">
      <font-face font-family="${escapeXml(family)}" units-per-em="1000" ascent="800" descent="200"/>
      <missing-glyph horiz-adv-x="1000"/>
      <glyph unicode="N" glyph-name="N" horiz-adv-x="1000" d="${escapeXml(glyphPath)}"/>
    </font>
  </defs>
  <rect width="1100" height="720" fill="#f3f1ea"/>
  <text x="80" y="92" fill="#181818" font-family="Inter, Arial, sans-serif" font-size="34" font-weight="700">${escapeXml(family)}</text>
  <text x="80" y="132" fill="#66645f" font-family="Inter, Arial, sans-serif" font-size="18">Single-glyph export for uppercase N</text>
  <g transform="translate(80 178)">
    <rect width="420" height="420" rx="8" fill="#ffffff" stroke="#dfddd5"/>
    <text x="210" y="300" text-anchor="middle" fill="#181818" font-family="${escapeXml(family)}, Inter, Arial, sans-serif" font-size="260">N</text>
    <text x="28" y="382" fill="#66645f" font-family="Inter, Arial, sans-serif" font-size="15">Font proof glyph</text>
  </g>
  <g>
    <rect x="540" y="100" width="360" height="500" rx="8" fill="#ffffff" stroke="#dfddd5"/>
    ${embeddedSource}
    <text x="568" y="570" fill="#66645f" font-family="Inter, Arial, sans-serif" font-size="15">Source SVG mark</text>
  </g>
</svg>`;
}

function extractPathData(svg) {
  return Array.from(svg.matchAll(/\sd=["']([^"']+)["']/gi))
    .map((match) => match[1].trim())
    .filter(Boolean)
    .slice(0, 24);
}

function sanitizeSvgContent(value) {
  const content = String(value || "").trim();
  const start = content.indexOf("<svg");
  const end = content.lastIndexOf("</svg>");
  if (start === -1 || end === -1 || end <= start) return "";

  const svg = content.slice(start, end + 6);
  if (/<script\b/i.test(svg)) return "";
  if (/<foreignObject\b/i.test(svg)) return "";
  if (/\son[a-z]+\s*=/i.test(svg)) return "";
  if (/javascript:/i.test(svg)) return "";
  if (/\b(?:href|src)\s*=\s*["']https?:/i.test(svg)) return "";
  return svg;
}

function slugify(value) {
  return String(value || "artifact")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "")
    .slice(0, 80) || "artifact";
}

function titleCase(value) {
  return String(value || "artifact")
    .replace(/[/_-]+/g, " ")
    .replace(/\s+/g, " ")
    .trim()
    .slice(0, 80);
}

function escapeXml(value = "") {
  return String(value)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#039;");
}
